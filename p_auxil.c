#include "minishell.h"

int	ft_islnu(int c)
{
	return ((c >= '0' && c <= '9')
		|| (c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z')
		|| (c == '_'));
}

int	ft_islnudsq(int c)
{
	return ((c >= '0' && c <= '9')
		|| (c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z')
		|| (c == '_') || (c == '.') || (c == '$' || (c == '?'))
		|| (c == '/') || (c == 39) || (c == 34) || (c == '|'));
}

int	ft_isspace(int c)
{
	return (((c >= 9) && (c <= 13)) || (c == 32));
}

int	ft_islnudsqi(int c)
{
	return (((c >= '0' && c <= '9')
			|| (c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z')
			|| (c == '_') || (c == '.') || (c == '?'))
		|| (c == '/') || (c == 39) || (c == 34));
}
